// 7 - Ler um valor e escrever a mensagem É MAIOR QUE 10! se o valor lido for maior que 10,
//caso contrário escrever NÃO É MAIOR QUE 10!

public class ex_7 {
    public static void main (String[] out) {
        int valor = 9;
        if (valor > 10) {
            System.out.println("É MAIOR QUE 10!");
        }
        else if (valor < 10) {
            System.out.println("É MENOR QUE 10!");
        }
        else {
            System.out.println("É IGUAL A 10!");
        }
    }
}

