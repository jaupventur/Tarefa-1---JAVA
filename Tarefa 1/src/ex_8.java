// 8 - Ler um valor e escrever se é positivo ou negativo (considere o valor zero como positivo).

public class ex_8 {
    public static void main(String[] args) {
        int valor = 0;
        if (valor < 0 ) {
            System.out.println("Valor é negativo");
        }
        else {
            System.out.println("Valor é positivo");
        }

    }
}
